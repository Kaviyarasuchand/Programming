# Using a for loop without the range function
# Example 1: Printing numbers from 1 to 5
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)

# Example 2: Printing elements of a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Example 3: Iterating over characters in a string
text = "hello"
for char in text:
    print(char)
