# Using range function without a for loop
# Example 1: range(stop)
numbers1 = list(range(5))
print("Example 1:", numbers1)  # Output: [0, 1, 2, 3, 4]

# Example 2: range(start, stop)
numbers2 = list(range(1, 6))
print("Example 2:", numbers2)  # Output: [1, 2, 3, 4, 5]

# Example 3: range(start, stop, step)
numbers3 = list(range(1, 10, 2))
print("Example 3:", numbers3)  # Output: [1, 3, 5, 7, 9]
