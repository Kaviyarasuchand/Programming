# Integer
x_int = 10

# Float
y_float = 3.14

# String
z_str = "Hello, world!"

# Boolean
a_bool = True

# List
b_list = [1, 2, 3, 4, 5]

# Tuple
c_tuple = (1, 2, 3, 4, 5)

# Set
d_set = {1, 2, 3, 4, 5}

# Dictionary
e_dict = {"name": "Alice", "age": 30}

# Complex
f_complex = 2 + 3j

# Bytes
g_bytes = b"Hello"

# Bytearray
h_bytearray = bytearray(b"Hello")

# NoneType
i_none = None

# Displaying the data types
print("Integer:", x_int, type(x_int))
print("Float:", y_float, type(y_float))
print("String:", z_str, type(z_str))
print("Boolean:", a_bool, type(a_bool))
print("List:", b_list, type(b_list))
print("Tuple:", c_tuple, type(c_tuple))
print("Set:", d_set, type(d_set))
print("Dictionary:", e_dict, type(e_dict))
print("Complex:", f_complex, type(f_complex))
print("Bytes:", g_bytes, type(g_bytes))
print("Bytearray:", h_bytearray, type(h_bytearray))
print("NoneType:", i_none, type(i_none))
