# Integer to float
x_int = 10
y_float = float(x_int)
print("Integer to Float:", y_float)

# Float to integer
a_float = 3.14
b_int = int(a_float)
print("Float to Integer:", b_int)

# Integer to string
c_str = str(x_int)
print("Integer to String:", c_str)

# Float to string
d_str = str(a_float)
print("Float to String:", d_str)

# String to integer
e_str = "20"
f_int = int(e_str)
print("String to Integer:", f_int)

# String to float
g_str = "3.14"
h_float = float(g_str)
print("String to Float:", h_float)

# Boolean to integer
i_bool = True
j_int = int(i_bool)
print("Boolean to Integer:", j_int)

# Integer to boolean
k_int = 0
l_bool = bool(k_int)
print("Integer to Boolean:", l_bool)

# String to boolean
m_str = "True"
n_bool = bool(m_str)
print("String to Boolean:", n_bool)

# List to tuple
o_list = [1, 2, 3]
p_tuple = tuple(o_list)
print("List to Tuple:", p_tuple)

# Tuple to list
q_tuple = (4, 5, 6)
r_list = list(q_tuple)
print("Tuple to List:", r_list)
